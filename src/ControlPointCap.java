    private void inicializar() {
        for (int i = 0; i < caps.length; i++) {
            caps[i] = new Caps();
        }
    }

    private void mostrarDatos() {
        TAInfo.setText("");
        for (Caps cp : caps) {
            if (cp.getSilueta() != null) {
                TAInfo.append(cp.toString() + "\n");
            }

        }
    }
    
