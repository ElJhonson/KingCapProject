    private void BAnadirCapActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BAnadirCapActionPerformed
        talla = TFTalla.getText();
        modelo = TFModelo1.getText();
       precio = (int)SPrecio.getValue();
        
        if ((silueta != null) && !(talla.isBlank()) && !(modelo.isBlank()) && precio>0) {
            if(contCaps<10){
                caps[pos++] = new Caps(silueta, talla, modelo, precio);
                clearElements();
                mostrarDatos();
                contCaps++;
            }else{
                JOptionPane.showMessageDialog(null, "Excediste el numero de productos");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingresa los datos correctos solicitados");
            
        }


    }//GEN-LAST:event_BAnadirCapActionPerformed

