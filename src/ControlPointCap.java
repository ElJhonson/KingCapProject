    private void BCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCuentaActionPerformed
        for (Caps cap : caps) {
            if (cap.getPrecio() != 0 && cap.getSilueta() != null) {
                cuenta += cap.getPrecio();
            }
        }
        
        TFTotal.setText(String.valueOf(cuenta));
        cuentaOP = cuenta;
        cuenta = 0;
    }//GEN-LAST:event_BCuentaActionPerformed

    private void BCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BCancelarActionPerformed
        for (int i = caps.length-1; i > 0; i--) {
            if(caps[i].getModelo()!=null){
                caps[i] = new Caps();
                break;
            }
        }
        mostrarDatos();
    }//GEN-LAST:event_BCancelarActionPerformed

